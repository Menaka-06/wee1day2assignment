package week1.day2assignments;
//to convert negative number to positive number using condition
public class NegativeToPositive {

	public static void main(String[] args) {
		int num=-40,n=-1,r=0;
		if(num<0)
		{
		r=n*num; 
		System.out.println(num+" is converted to "+r);
		}
		else if(num>0)
		{
			r=n*num;
			System.out.println(num+" is converted to "+r);
		}
		else
			System.out.println("The number is zero");
	}

}
